use nalgebra::Point2;
use nalgebra::Vector2;

//************************************************************************************************
//************************************************************************************************
//************************************************************************************************
#[derive(Debug, Copy, Clone)]
pub struct Transform {
    pub pos: Point2<i32>,
    pub size: Vector2<u32>,
}

//************************************************************************************************
impl Transform {
    pub fn new(pos: &Point2<i32>, size: &Vector2<u32>) -> Self {
        return Self {
            pos: *pos,
            size: *size,
        };
    }
}
